def add_norwegian_names():
    """Legger til norske landnavn i countries_data.json"""
    
    # Les eksisterende data
    with open('countries_data.json', 'r', encoding='utf-8') as f:
        countries = json.load(f)
    
    # Legg til norske navn
    for country in countries:
        english_name = country.get('name', '')
        norwegian_name = norwegian_names.get(english_name, english_name)
        country['name_no'] = norwegian_name
    
    # Lagre til ny fil
    with open('countries_data_no.json', 'w', encoding='utf-8') as f:
        json.dump(countries, f, indent=2, ensure_ascii=False)
    
    print(f"‚úÖ Lagt til norske navn for {len(countries)} land")
